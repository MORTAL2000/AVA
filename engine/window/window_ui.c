void ui_demo() {
    /* GUI */
    if (nk_begin(ui_ctx, "Demo", nk_rect(50, 50, 230, 250),
        NK_WINDOW_BORDER|NK_WINDOW_MOVABLE|NK_WINDOW_SCALABLE|
        NK_WINDOW_MINIMIZABLE|NK_WINDOW_TITLE))
    {
        enum {EASY, HARD};
        static int op = EASY;
        static int property = 20;
        static struct nk_colorf bg = { 0.10f, 0.18f, 0.24f, 1.00f };

        nk_layout_row_static(ui_ctx, 30, 80, 1);
        if (nk_button_label(ui_ctx, "button"))
            printf("button pressed!\n");
        nk_layout_row_dynamic(ui_ctx, 30, 2);
        if (nk_option_label(ui_ctx, "easy", op == EASY)) op = EASY;
        if (nk_option_label(ui_ctx, "hard", op == HARD)) op = HARD;
        nk_layout_row_dynamic(ui_ctx, 22, 1);
        nk_property_int(ui_ctx, "Compression:", 0, &property, 100, 10, 1);

        nk_layout_row_dynamic(ui_ctx, 20, 1);
        nk_label(ui_ctx, "background:", NK_TEXT_LEFT);
        nk_layout_row_dynamic(ui_ctx, 25, 1);
        if (nk_combo_begin_color(ui_ctx, nk_rgb_cf(bg), nk_vec2(nk_widget_width(ui_ctx),400))) {
            nk_layout_row_dynamic(ui_ctx, 120, 1);
            bg = nk_color_picker(ui_ctx, bg, NK_RGBA);
            nk_layout_row_dynamic(ui_ctx, 25, 1);
            bg.r = nk_propertyf(ui_ctx, "#R:", 0, bg.r, 1.0f, 0.01f,0.005f);
            bg.g = nk_propertyf(ui_ctx, "#G:", 0, bg.g, 1.0f, 0.01f,0.005f);
            bg.b = nk_propertyf(ui_ctx, "#B:", 0, bg.b, 1.0f, 0.01f,0.005f);
            bg.a = nk_propertyf(ui_ctx, "#A:", 0, bg.a, 1.0f, 0.01f,0.005f);
            nk_combo_end(ui_ctx);
        }
    }
    nk_end(ui_ctx);

    overview(ui_ctx);
}

